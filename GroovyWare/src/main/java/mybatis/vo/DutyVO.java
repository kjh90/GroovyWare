package mybatis.vo;

public class DutyVO {
	private String duty_code,duty_name,duty_rank,duty_use;

	public String getDuty_code() {
		return duty_code;
	}

	public void setDuty_code(String duty_code) {
		this.duty_code = duty_code;
	}

	public String getDuty_name() {
		return duty_name;
	}

	public void setDuty_name(String duty_name) {
		this.duty_name = duty_name;
	}

	public String getDuty_rank() {
		return duty_rank;
	}

	public void setDuty_rank(String duty_rank) {
		this.duty_rank = duty_rank;
	}

	public String getDuty_use() {
		return duty_use;
	}

	public void setDuty_use(String duty_use) {
		this.duty_use = duty_use;
	}
	
	
	
}
